function logBlock(fruit,car,prefferedDevice){     //function declaration
    console.log("Fruit: "+fruit);
    console.log("Car: "+car);
    console.log("Device: "+prefferedDevice);
}

logBlock("Banana","Jeep","Laptop");  //function call
console.log("First ended");
logBlock("Apple","Mercedes","iPad");  //function call
console.log("Second ended");
logBlock("Pear","BMW","Phone"); //function call
